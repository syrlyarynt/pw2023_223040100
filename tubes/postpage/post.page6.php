    <h2 class="sub-heading">Pesawat</h2>
    <p class="post-text">Apa itu Pesawat Terbang? Secara umum, pengertian pesawat terbang adalah kendaraan bermesin yang dapat terbang di udara.</p>
    <p class="post-text">Berdasarkan Undang-undang Nomor 1 Tahun 2009 tentang Penerbangan, menyebutkan pesawat terbang adalah pesawat udara yang lebih berat dari udara, bersayap tetap dan dapat terbang dengan tenaganya sendiri.</p>
    <p class="post-text">Sebagian orang menganggap jika pesawat terbang dan pesawat udara memiliki pengertian yang smaa. Namun menurut dunia penerbangan, keduanya memiliki pengertian berbeda.</p>
    <p class="post-text">Penyebutan pesawat udara memiliki pengertian yang luas karena tidak hanya menyebutkan tentang pesawat terbang saja melainkan kendaraan udara jenis lainnya seperti helikopter.</p>
    <h2 class="sub-heading">Penemu dan Sejarah Pesawat</h2>
    <p class="post-text">Orville Wright dan Wilbur Wright atau yang akrab disapa dengan Wright Bersaudara adalah penemu, perancang sekaligus penerbang pesawat terbang yang lebih berat dari udara untuk pertama kalinya.</p>
    <p class="post-text">Pesawat yang dimaksud adalah Flayer yang berhasil diluncurkan pada tahun 1903 di Amerika Serikat.</p>
    <p class="post-text">Kemudian, pada tahun 1910 seorang berkebangsaan Inggris bernama Samuel F Cody juga tercatat sebagai penemu pesawat sekaligus melangsungkan aksinya untuk pertama kali di lapangan Farnborough.</p>
    <p class="post-text">Jauh sebelum itu, penemuan akan pesawat yang lebih ringan dari udara yang diaplikasikan lewat balon udara panas berhasil ditemukan pertama kali oleh kebangsaan Prancis pada tahun 1782 yakni Etiene Montgolfier dan Joseph Montgolfier.</p>
    <p class="post-text">Penemuan tersebut kemudian disempurnakan oleh Ferdinand von Zeppelin dengan memodifikasinya berbentuk cerutu dan digunakan untuk membawa penumpang dan barang, tepatnya pada tahun 1900 di Jerman.</p>
    <p class="post-text">Balon Zeppelin ini berhasil menguasai pengangkutan udara sampai akhirnya terjadi tragedi kapal Zeppelin dalam perjalanan trans-Atlantik pada tahun 1936 di New Jersey. Tragedi tersebut juga menandakan berakhirnya kekuasaan Zeppelin meskipun balon udara yang dirancangnya masih terpakai hingga menjalang Perang Dunia II.</p>
    <p class="post-text">Kembali pada Wright Bersaudara, dimana pada masa ini pesawat terbang terus mengalami modifikasi dan juga rancang bangun baik dari sisi mesin maupun bentuknya. Hingga dapat digunakan sebagai alat transportasi dan berbagai kebutuhan lainnya. Perlu diketahui jika pesawat komersial yang lebih besar berhasil dibuat dengan nama Bristol Brabazon pada tahun 1949.</p>
    <p class="post-text">Perkembangan terus dilakukan sampai saat ini. Maka dari itu pesawat terbang memiliki berbagai jenis sesuai kebutuhan dan kepentingan tertentu.</p>
    <p class="post-text">Pesawat terbang dengan penumpang terbesar di dunia dibuat oleh airbus industrie dari Eropa dengan pesawat A380.</p>
