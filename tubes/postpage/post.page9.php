    <h2 class="sub-heading">Website</h2>
    <p class="post-text">Website adalah serangkaian halaman web berisi informasi yang terhubung satu sama lain dan diakses melalui internet. Pada era digital saat ini, website telah menjadi salah satu elemen penting di dalam kehidupan manusia.</p>
    <p class="post-text">Bagi pengunjung, website memberikan akses yang mudah dan cepat untuk mencari informasi, membeli produk, atau mendapat pengalaman baru. Sedangkan bagi pelaku bisnis, website dapat meningkatkan branding perusahaan serta memfasilitasi penjualan produk secara online.</p>
    <h2 class="sub-heading">Sejarah Website</h2>
    <p class="post-text">Website pertama di dunia diluncurkan pada tahun 1991 oleh ahli komputer asal Inggris bernama Sir Timothy John “Tim” Berners-Lee. Awalnya, tujuan Tim dalam merancang website adalah untuk memfasilitasi pertukaran dan pembaruan informasi antar sesama peneliti di tempat kerjanya.</p>
    <p class="post-text">Itu karena, website tersebut hanya dibangun menggunakan HTML (HyperText Markup Language). HTML adalah bahasa markup yang hanya mampu menampilkan teks dan gambar yang statis.</p>
    <p class="post-text">Baru setelahnya pada tahun 1996, muncul inovasi bernama CSS (Cascading Style Sheets). CSS adalah teknologi yang diperkenalkan untuk mempercantik tampilan website.</p>
    <p class="post-text">Di tahun yang sama, bahasa pemrograman JavaScript dirilis ke publik sebagai bahasa yang memungkinkan website menjadi lebih interaktif dan dinamis. Kemudian pada dekade 2000-an, teknologi Flash diluncurkan untuk membuat animasi dan video interaktif pada halaman web.</p>
    <p class="post-text">Inovasi lain dalam perkembangan website adalah munculnya responsive design. Website responsif adalah halaman situs yang dapat menyesuaikan diri dengan ukuran layar pengguna. Inovasi ini penting mengingat tren penggunaan smartphone dan tablet untuk mengakses internet.</p>
