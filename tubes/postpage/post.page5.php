    <h2 class="sub-heading">Laptop</h2>
    <p class="post-text">Laptop memiliki pengertian sebuah perangkat yang diciptakan untuk mengolah, menyimpan bahkan menciptakan data multimedia yang dikomandokan oleh sistem akun secara manual dan sering disebut komputer portabel yang memudahkan penggunanya agar dapat menggunakannya di mana saja.</p>
    <p class="post-text">Asal usul nama laptop diambil dari kata "lap" yang berarti "pangkuan" dan "top" yang berarti "atas" jika disatukan dapat diartikan sebagai "diatas pangkuan".</p>
    <p class="post-text">Arti lebih sederhananya yaitu komputer kecil yang dapat dilipat dan dapat digunakan di atas pangkuan. Nama yang diberikan sesuai dengan kelebihannya yaitu mudah dibawa ke mana saja.</p>
    <h2 class="sub-heading">Sejarah Laptop</h2>
    <p class="post-text">Sebelum Anda mengenal laptop, Anda terlebih dahulu harus mengetahui rangkaian-rangkaian peristiwa yang menjadi faktor munculnya sebuah laptop dan orang yang menciptakannya.</p>
    <p class="post-text">Jika ditelusuri lebih lanjut, laptop ini berasal dari teknologi sebelumnya yakni komputer. Komputer merupakan rangkaian dari beberapa alat elektronik yang dibuat khusus.</p>
    <p class="post-text">Kemudian alat-alat elektronik ini dihubungkan dengan rangkaian yang sudah disusun di dalam CPU dan ditampilkan pada monitor.</p>
    <p class="post-text">Komputer juga memiliki generasi-generasinya, mulai dari generasi pertama hingga generasi terakhir.</p>
    <p class="post-text">Pembuatan laptop sudah direncanakan di awal tahun 1970.  Hal ini terjadi karena penggunaan komputer terlalu terbatas.</p>
    <p class="post-text">Lalu, muncul ide untuk membuat komputer portabel atau laptop yang dikemukakan oleh Alan Kay.</p>
    <p class="post-text">Kemudian, seorang warga negara Amerika Serikat yang menjadi designer PC melanjutkan ide dari Alan. Designer PC (Personal Computer) ini bernama Adam Osborne.</p>
    <p class="post-text">Adam merealisasikan konsep Alan Kay ke dalam dunia nyata. sehingga lahirlah laptop pertama yang diciptakan Adam Osborne pada tahun 1975 dengan bantuan Felsenstein sebagai ilmuwan pembuat professor.</p>
    <p class="post-text">Komputer portabel ini dilahirkan dengan ukuran yang ringkas, bahkan jauh lebih ringkas dibanding komputer laptop ini bernama IBM Portable PC 500.</p>
