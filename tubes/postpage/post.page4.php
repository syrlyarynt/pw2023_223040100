    <h2 class="sub-heading">Kapal Laut</h2>
    <p class="post-text">Kapal merupakan transportasi laut yang mana digunakan sebagai pengangkut penumpang dan barang, sama seperti sampan atau perahu kecil.</p>
    <p class="post-text">Setidaknya terdapat 25 jenis kapal yang dikenal dalam dunia pelayaran. Mulai dari kapal kecil yang digunakan untuk khusus penumpang hingga kapal yang mempunyai ukuran raksasa yang mana digunakan untuk mengangkut minyak atau peralatan berat.</p>
    <h2 class="sub-heading">Sejarah dan Penemu Kapal Laut</h2>
    <p class="post-text">Ternyata kapal sendiri sudah ada pada zaman nenek moyang dulu tetapi, bentuk dari kapal tersebut masih sangat sederhana, yakni berupa rakit dan batang kayu.</p>
    <p class="post-text">Hal tersebut tentunya menjadi bukti bahwa dari dulu hingga saat ini, teknologi perkapalan terus berkembang, baik untuk kepentingan komersil, militer maupun politik dan sains.</p>
    <p class="post-text">Lantas siapa penemu kapal? Meskipun kapal sudah ada sejak zaman nenek moyang yang mana berupa rakit, namun penemu kapal sendiri yaitu Robert Fulton merupakan seorang penemu, ahli teknik, dan seniman berkebangsaan Amerika yang mana berhasil membuat kapal uap untuk pertama kalinya di dunia.</p>
    <p class="post-text">Kapal uap tersebut dibuat untuk melindungi pelabuhan New York dari seranga Inggris pada zaman perang.Kapal uap tersebut memiliki berat 2,745 ton dan memiliki panjang 156 kaki atau setara dengan 48 meter.</p>
    <p class="post-text">Kapal yang diluncurkan pada bulan Okteber 1924 ini diberi nama The Domologos atau Fulton. Bukan hanya kapal saja, Robert Fulton juga berhasil membuat kapal yang berupa transportasi lintas air dan kapal selam</p>
