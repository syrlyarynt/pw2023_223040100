    <h2 class="sub-heading">Radio</h2>
    <p class="post-text">Radio adalah teknologi yang digunakan untuk pengiriman sinyal dengan cara modulasi dan radiasi elektromagnetik (gelombang elektromagnetik). Gelombang ini melintas, dan merambat lewat udara, dan bisa juga merambat lewat ruang angkasa yang hampa udara, karena gelombang ini tidak memerlukan medium pengangkut (seperti molekul udara). Saat ini radio dapat didengarkan melalui ponsel pintar, berbeda halnya sebelum abad ke-20, ketika konsep nirkabel masih dianggap kisah fiksi semata.</p>
    <h2 class="sub-heading">Sejarah dan Perkembangan Radio</h2>
    <p class="post-text">Sejarah radio adalah sejarah teknologi yang menghasilkan peralatan radio yang menggunakan gelombang radio. Stasiun radio paling awal menggunakan sistem radiotelegrafi dan tidak membawa audio. Agar siaran audio dimungkinkan, perangkat deteksi dan amplifikasi elektronik harus digunakan.</p>
    <p class="post-text">Sejarah penemuan radio dimulai di Inggris dan Amerika Serikat. Donald Mc. Nicol dalam bukunya Radioâ€™s Conquest of Space menyatakan bahwa terkalahkannya ruang angkasa oleh radio dimulai tahun 1802 oleh Dane, yaitu dengan ditemukannya suatu pesan dalam jarak pendek dengan menggunakan alat sederhana berupa kawat beraliran listrik. Penemuan berikutnya adalah oleh tiga orang cendikiawan muda, di antaranya adalah James Maxwell berkebangsaan Inggris pada tahun 1865. Ia dijuluki scientific father of wireless, karena berhasil menemukan rumus-rumus yang diduga mewujudkan gelombang elektromagnetik, yakni gelombang yang digunakan radio dan televisi.</p>
    <p class="post-text">Pada tahun 1896 ilmuwan Italia, Guglielmo Marconi mendapat hak paten atas telegraf nirkabel yang menggunakan dua sirkuit. Pada saat itu sinyal ini hanya bisa dikirim pada jarak dekat. Namun, hal inilah yang memulai perkembangan teknologi radio. Pada tahun 1897, Marconi kembali mempublikasikan penemuan bahwa sinyal nirkabel dapat ditransmisikan pada jarak yang lebih jauh (12 mil (19.000 m)).</p>
    <p class="post-text">Selanjutnya, pada tahun 1899 Marconi berhasil melakukan komunikasi nirkabel antara Prancis dan Inggris lewat Selat Inggris dengan menggunakan osilator Tesla. John Ambrose Fleming pada tahun 1904 menemukan bahwa tabung audion dapat digunakan sebagai receiver nirkabel bagi teknologi radio ini. Dua tahun kemudian ((Kanuri) 1901) Dr. Lee De Forest menemukan tabung elektron yang terdiri dari tiga elemen (triode audion). Penemuan ini memungkinkan gelombang suara ditransmisikan melalui sistem komunikasi nirkabel. Tetapi sinyal yang ditangkap masih sangat lemah.</p>
    <p class="post-text">Barulah pada tahun 1912 Edwin Howard Armstrong menemukan penguat gelombang radio disebut juga radio amplifier. Alat ini bekerja dengan cara menangkap sinyal elektromagnetik dari transmisi radio dan memberikan sinyal balik dari tabung. Dengan begitu kekuatan sinyal akan meningkat sebanyak 20.000 kali per detik. Suara yang ditangkap juga jauh lebih kuat sehingga bisa didengar langsung tanpa menggunakan earphone. Penemuan ini kemudian menjadi sangat penting dalam sistem komunikasi radio karena jauh lebih efisien dibandingkan alat terdahulu. Meskipun demikian hak paten atas amplifier jatuh ke tangan Dr. Lee De Forest.</p>
    <p class="post-text">Penggunaan radio sebagai alat atau media komunikasi massa pada awalnya diperkenalkan oleh David Sarnoff pada tahun 1915. Selanjutnya Le De Forrest melalui eksperimen siaran radionya, yang telah menyiarkan kampanye pemilihan presiden Amerika Serikat pada tahun 1916, sehingga ia dikenal sebagai pelopor penyiaran radio.</p>
    <p class="post-text">Awalnya sinyal pada siaran radio ditransmisikan melalui gelombang data yang kontinu baik melalui modulasi amplitudo (AM), maupun modulasi frekuensi (FM). Metode pengiriman sinyal seperti ini disebut analog. Selanjutnya, seiring perkembangan teknologi ditemukanlah internet, dan sinyal digital yang kemudian mengubah cara transmisi sinyal radio.</p>
