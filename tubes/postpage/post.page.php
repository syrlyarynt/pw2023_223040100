    <h2 class="sub-heading">Smartphone</h2>
    <p class="post-text">Smartphone merupakan gadget genggam elektronik yang mencakup fungsionalitas lanjutan selain melakukan panggilan telepon dan mengirim pesan teks.
      Hal ini menjelaskan bahwasanya smartphone memiliki kelebihan tertentu dalam memberikan pelayanan terhadap penggunanya. Misalnya saja seperti iPhone atau ponsel berbasis Android lainnya, yang mana dapat menjalankan aplikasi pihak ketiga dengan menyediakan fungsionalitas tanpa batas. </p>
    <p class="post-text">Dalam jaringan komunikasi seperti Internet, smartphone sendiri bekerja dengan memanfaatkan gelombang radio digital. Dalam hal ini yang membuatnya mampu menangani sejumlah informasi yang diterima dan dikirim melalui perangkat digital satu ke lainnya menggunakan teknologi yang disebut FSK (Frequency Shift Keying). </p>
    <p class="post-text">Pada dasarnya smartphone Anda adalah radio mini yang terus-menerus menerima sinyal lain dari jaringan seluler terpisah ke dalam gelombang elektronik yang berbeda-beda. Masing-masing gelombang tersebut memiliki sinyal antena yang mengirimkan gelombang seluler terhadap perangkat elektronik pada area tertentu.</p>
    <h2 class="sub-heading">Sejarah dan Perkembangan Smartphone</h2>
    <p class="post-text">Dengan perkembangan internet yang semakin maju, smartphone menjadi sebuah gadget praktis yang populer karena memiliki banyak fitur dan kemampuan yang membuatnya seperti sebuah ukuran mini dari komputer. Namun tahukah Anda bahwa awalnya smartphone hanya dapat digunakan sebagai media pengirim dan penerima pesan, untuk lebih jelasnya silakan simak perkembangan smartphone dari masa ke masa berikut ini.</p>
    <p class="post-text">Tahun 1992 Sekitar 25 tahun lalu perusahaan teknologi IBM secara luas dikreditkan sebagai perusahaan yang mengembangkan smartphone pertama di dunia. Produk digital pertama ini diberi nama Simon dan mulai dijual secara global pada tahun 1994 dengan memberikan fitur layar sentuh kepada penggunanya.</p>
    <p class="post-text">Tahun 2001 Barulah pada tahun 2000an smartphone mendukung Jaringan 3G yang memungkinkannya sebagai perangkat elektronik portabel pertama yang mampu mengakses Internet dengan sistem wireless. Namun tentunya akses internet pada sebuah ponsel pada masa itu masih tergolong mahal dan tidak sepadan dari kecepatan akses data yang diberikan apabila dibandingkan dengan masa sekarang.</p>
    <p class="post-text">Tahun 2007 Revolusi smartphone modern baru benar-benar dimulai ketika Steve Jobs meluncurkan iPhone pertama di tahun 2007. Apabila model ponsel sebelumnya mengandalkan keypad dan hanya dapat menavigasi versi internet yang sederhana dengan menggunakan papan tombol. Namun berbeda dengan iPhone yang memungkinkan pengguna untuk dapat memakai layar sentuh dengan sistem serta fitur yang paling mendekati komputer desktop di masanya.</p>
    <p class="post-text">Tahun 2010 Smartphone telah menghadirkan fitur-fitur yang mulai dapat dijangkau oleh masyarakat menengah melalui Android dan menjadi populer dengan ketersediaan model dan merek yang bervariasi.</p>
    <p class="post-text">Tahun 2018 Diperkirakan penggunaan smartphone akan melampaui lima miliar dan pengguna sudah mulai bertebaran dimana-mana serta menggunakan smartphone untuk lebih dari sekadar menelepon dan mengirim pesan, namun juga digunakan untuk belajar, mencari pekerjaan, membaca berita, menonton film, bersosialisasi dan masih banyak lagi. Pada tahap ini manusia sudah tidak mampu untuk hidup tanpa smartphone bahkan untuk sesaat.<p>
